    <h1>Basic Qualifications</h1>
    <p>Some of the conditions are : </p>
    <strong>Age : </strong>
    16 or older for whole blood or apheresis donations (parental permission is required for all 16-year-old donors and, in some states, for 17-year-old donors.)
    <br/>
    <strong>Weight : </strong>
    At least 110 pounds (50 kg)
    There are some weight and height restrictions for <strong>donors younger than 23.</strong>
    <br/>
    <strong>Waiting period between donations: </strong>
    <div class="list">
        <ul class="waiting_periods">
            <li id="1">Whole blood or red cell/plasma donations: 56 days (up to 6x/12 months)</li>
            <li id="2">Plasma donations: 28 days</li>
            <li id="3">Double red cell donations: 112 days (up to 3x/12 months)</li>
            <li id="4">Platelet donations: 7 days (up to 24x/12 months)</li>
        </ul>
    </div>
    <div class="list">
        <h1>When you are not allowed to donate</h1>
        <p>Some of the conditions are : </p>
        <ul class="no_donation">
            <li id="1">Have cold or flu symptoms or do not feel well on the day of donation.</li>
            <li id="2">Had hepatitis after your 11th birthday.</li>
            <li id="3">Have had close contact with someone with clinical</li>
            <li id="4">hepatitis within the past 12 months.</li>
            <li id="5">Have a history of cancer in the past year (except some skin or in situ cancers).</li>
            <li id="6">Had a blood transfusion, ear/skin piercing, acupuncture, accidental needlestick, or come in contact with someone elseâ€™s blood in the past 12 months. (Ear/skin piercing and acupuncture procedures may be acceptable depending the technique used.)</li>
            <li id="7">Are donating at Blood Centers of the Pacific and have had a tattoo in the past 12 months.</li>
            <li id="8">At United Blood Services locations, tattoos may be acceptable if done at a regulated business in an approved state. Contact your local center for a list of approved states.</li>
            <li id="9">Had malaria in the past or traveled to a malarial risk area in the past 12 months.</li>
            <li id="10">Are or have been pregnant in the past 6 weeks.</li>
            <li id="11">Have been treated for syphilis or gonorrhea in the past 12 months.</li>
            <li id="12">Have used a needle to inject drugs not prescribed for you (including steroids).</li>
            <li id="13">Are at risk for exposure to HIV, the virus that causes AIDS.</li>
        </ul>
    </div>
